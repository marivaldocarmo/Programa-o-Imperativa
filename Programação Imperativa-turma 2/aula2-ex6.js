/*Criar as 4 function expressin (dentro de variaveis) basicas de uma 
calculadora:*/
let soma=function(n1,n2)
{
    return n1+n2;
}
let sub=function(n1,n2)
{
    return n1-n2;
}
let mult=function(n1,n2)
{
    return n1*n2;
}
let div=function(n1,n2)
{
    return n1/n2;
}
console.log(soma(5,5));
console.log(sub(5,5));
console.log(mult(5,5));
console.log(div(5,5));
